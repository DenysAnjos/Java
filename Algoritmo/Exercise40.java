package exercises.one.to.a.hundred;

public class Exercise40 {

	public static void main(String[] args) {
		int n = 0;
		System.out.print("Conting:");
		while (n <= 18) {
			System.out.print(n + " ");
			n = n + 3;
		}
		System.out.print("End!");
	}

}
