package exercises.one.to.a.hundred;

public class Exercise39 {

	public static void main(String[] args) {
		int n = 10;
		System.out.print("Counting:");
		while(n>2) {
			System.out.print(n + " ");
			n--;
		}
		System.out.println("End!");
	}

}
